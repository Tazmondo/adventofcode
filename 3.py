map = []

for line in input.splitlines(False):
    if line == "":
        continue
    line = line.strip()
    newmap = []
    for char in line:
        if char == ".":
            newmap.append(False)
        elif char == "#":
            newmap.append(True)
    map.append(newmap)

slopes = (
    (1, 1),
    (3, 1),
    (5, 1),
    (7, 1),
    (1, 2)
)
out = []
for slope in slopes:
    posx, posy = 0-slope[0], 0-slope[1]
    trees = 0
    while posy + slope[1] < len(map):
        posx = (posx + slope[0]) % 31
        posy += slope[1]
        if map[posy][posx]:
            trees += 1

    print(f"Right {slope[0]}, Down {slope[1]}, encounters {trees} trees.")
    out.append(trees)

outmp = 1
